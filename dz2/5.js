function fplus(a, b) {
    return a + b;
}

function minus(a, b) {
    return a - b;
}

function division(a, b) {
    return a / b;
}

function multiplication(a, b) {
    return a * b;
}

function module(a, b) {
    return a % b;
}

console.log(fplus(3, 4));
console.log(minus(8, 4));
console.log(division(6, 3));
console.log(multiplication(3, 4));
console.log(module(4, 4));



